uint16_t crc16_ccitt_update(uint16_t crc, uint8_t data)
{
#if (BUILD_USE_FAST_CRC == 1)
    crc = (crc << 8) ^ crc16CcittTable[(crc >> 8) ^ data];
#else
    crc = crc ^ ((uint16_t)data << 8);
    for (uint8_t i = 0; i < 8; i++)
    {
        if (crc & 0x8000)
            crc = (crc << 1) ^ 0x1021;
        else
            crc <<= 1;
    }
#endif
    return crc;
}

uint16_t crc16_ccitt(uint8_t *pData, uint16_t size)
{
    uint16_t crc = 0xFFFF;

    while (size--)
        crc = crc16_ccitt_update(crc, *pData++);

    return crc;
}

uint16_t crc16_update(uint16_t crc, uint8_t data)
{
#if (BUILD_USE_FAST_CRC == 1)
    crc = (crc >> 8) ^ crc16Table[(crc & 0xFF) ^ data];
#else
    crc ^= (uint16_t)data;

    for (uint8_t i = 0; i < 8; i++)
    {
        if (crc & 1)
            crc = (crc >> 1) ^ 0xA001;
        else
            crc = (crc >> 1);
    }
#endif    
    return crc;
}

uint16_t crc16(uint8_t *pData, uint16_t size)
{
    uint16_t crc = 0xFFFF;

    while (size--)
        crc = crc16_update(crc, *pData++);

    return crc;
}

